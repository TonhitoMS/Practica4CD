    private void mostrarTaboa(ArrayList<String> empresas, ArrayList<Double> valores){
        ModeloTaboa m;

        m = (ModeloTaboa) taboa.getModel();
        m.setFilas(empresas, valores);
        if (m.getRowCount() > 0) {
            taboa.setRowSelectionInterval(0, 0);
        }
    }
    
